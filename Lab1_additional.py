import os
import time

# 自动检测操作系统并选择合适的清屏命令 / Автоматическое определение ОС и выбор подходящей команды очистки
def clear_screen():
    # Windows系统使用'cls'，Linux/Mac使用'clear' / Windows использует 'cls', Linux/Mac используют 'clear'
    os.system('cls' if os.name == 'nt' else 'clear')

# 定义三个动画帧（这里用简单的ASCII艺术作为例子） / Определение трех кадров анимации (здесь используются простые ASCII-рисунки)
frame1 = """
  ★
 /|\\
 / \\
这是一个笑脸! 
Это улыбающееся лицо!
"""

frame2 = """
  ○
 /|\\
 / \\
笑脸眨眼了! 
Улыбающееся лицо моргает!
"""

frame3 = """
  ●
 /|\\
 / \\
眼睛闭上了! 
Глаза закрыты!
"""

# 将三个帧放入列表中 / Помещаем три кадра в список
frames = [frame1, frame2, frame3]

# 设置动画播放次数 / Устанавливаем количество повторений анимации
animation_cycles = 5

print("开始播放动画... \nНачало анимации...")

# 主动画循环 / Главный цикл анимации
for i in range(animation_cycles):
    for frame in frames:
        clear_screen()  # 清除屏幕 / Очистка экрана
        print(frame)    # 显示当前帧 / Отображение текущего кадра
        time.sleep(0.5) # 暂停0.5秒 / Пауза 0.5 секунды
        
print("动画播放完毕! \nАнимация завершена!")