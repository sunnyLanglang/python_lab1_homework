# 从y=8到y=0绘制图形（控制台从上到下输出）
for y in range(9, -1, -1):
    # 计算对应的x值
    x = y * 2
    
    # 构建每一行的显示
    if y > 0:
        # 绘制y轴和函数点
        line = f"{y:2d}│" + " " * (x) + "•"
        print(line)
    else:
        # 最后一行绘制x轴
        print(" 0\u2514" + "\u2500" * 18 + "\u2192 x")
        print("   0 2 4 6 8 1012141618")